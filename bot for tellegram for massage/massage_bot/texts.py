import html


def format_price(price_uah: int) -> str:
    """Ціна для показу: 0 означає «обговорюється на місці»."""
    return "за домовленістю" if price_uah <= 0 else f"{price_uah} грн"


def format_total(base_price: int, addon_price: int = 0) -> str:
    """Підсумок: якщо базова послуга «за домовленістю» — підсумок теж."""
    if base_price <= 0:
        return "за домовленістю"
    return f"{base_price + addon_price} грн"


def client_contact_block(
    user_id: int,
    username: str | None = None,
    phone: str | None = None,
) -> str:
    """HTML-блок для ідентифікації клієнта в повідомленнях майстру.

    Дозволяє звʼязатися в один тап: @username або посилання tg://user?id=…,
    яке відкриває чат із клієнтом навіть без username. Повідомлення з цим
    блоком треба слати з parse_mode="HTML".
    """
    lines: list[str] = []
    if phone:
        lines.append(f"📱 Телефон: {html.escape(phone)}")
    if username:
        lines.append(f"✍️ Звʼязок: @{html.escape(username)}")
    else:
        lines.append(
            f'✍️ Звʼязок: <a href="tg://user?id={int(user_id)}">відкрити чат у Telegram</a>'
        )
    return "\n".join(lines)


